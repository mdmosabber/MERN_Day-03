// Simple function declaration
function addition(){    
    var a = 10;
    var b = 30;
    var sum = a + b;
    console.log(sum);
}

// Simple function calling
addition();

// Parameterized function declaration
function subtraction(a, b){
    var sub = a - b;
    console.log(sub);
}

// Parameterized function calling
subtraction(50, 10);